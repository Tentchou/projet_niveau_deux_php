            <h2 class="headline-1 section-title">Quoi de bien que la science</h2>

            <p class="section-text">
                La Faculté des Sciences (FS) de l’Université de Douala (UD) est située au CAMPUS de PK17. Ce campus abrite le Décanat et les salles de cours. . La Faculté dispose aussi d’un amphithéâtre au campus I en face du gymnase. Elle bénéficie par ailleurs de quelques locaux à l’ancien Rectorat à Akwa qui abritent notamment le CEPAMOQ (Centre de Physique Atomique Moléculaire et Optique Quantique). Il convient également de signaler plusieurs salles de cours « Extension » et de deux amphithéâtres de 500 places que la Faculté des Sciences partage avec les autres Facultés de l’Université de Douala au campus II à NDOGBONG.
                La Faculté des Sciences comporte trois Cycles successifs d’études :
                • Le Premier Cycle ou Cycle de Licence ;
                • le Deuxième Cycle ou Cycle de Master;
                • le Cycle de Doctorat.
                Le Cycle de Licence d’une durée de trois (3) ans ou six (6) semestres et couvrant les niveaux I à III (semestres I à VI) du cursus universitaire dans la Faculté comprend : des enseignements fondamentaux ; des enseignements de formation bilingue ; des enseignements de professionnalisation.
                Le Deuxième Cycle ou Master, d’une durée de deux (2) ans ou quatre semestres, couvre les niveaux IV et V (semestres VII à X) du cursus universitaire dans la Faculté.
                Le Cycle de Doctorat. Par ailleurs, le CEPAMOQ assure une formation doctorale à partir du Master 2 en Physique de la Matière et du Rayonnement.
            </p>

            

